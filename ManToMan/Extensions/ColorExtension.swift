import Foundation
import SwiftUI

extension Color {
    // 피그마에 등록된 색상 보시고, 사용하시면 됩니다!
    // foregroundColor(.background) 이런식으로 사용하시면 돼요!
    
    //custom 색상
    static let background = Color("background")
    static let customDarkGray = Color("darkGray")
    static let customLightGray = Color("lightGray")
    
    //main color
    static let mainBlue = Color("mainBlue")
    static let mainRed = Color("mainRed")

    //disabled color
    static let disabledBlack = Color("disabledBlack")
    static let disabledBlue = Color("disabledBlue")

}
